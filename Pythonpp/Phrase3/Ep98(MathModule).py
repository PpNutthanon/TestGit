#Math Module
print(min(3,4,5,-40))
print(max(2,3,0,20))
print(abs(-50))
print(pow(5,3)) #5ยกกำลัง3

import math
print(math.sqrt(2)) #หารากที่สอง
print(math.floor(80.4)) #ปัดเศษลง
print(math.ceil(80.4)) #ปัดเศษขึ้น
print(math.pi)

#Trigonometric
print(math.sin(30)) #หน่วยที่ใส่เป็น Radian
convert=math.radians(90)
print(convert)
print(math.sin(convert)) #เปลี่ยน Radian เป็น Degree

#Distance Between Point to Point
point1=[2,-3]
point2=[-7,4]
print(math.dist(point1,point2))

#Logarithm
print(math.log10(1000))
print(math.log2(10))

