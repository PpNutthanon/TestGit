x,y,z = 10,5,11
print("x is more than y :",x>y)
print("x is less than y :",x<y)
print("x is equal to y :",x==y) #ต้องใส่==
print("x is not equal to y :",x!=y) #!=คือ ไม่เท่ากับ
print("x is more than or equal y :",x>=y)
print("x is less than or equal to y",x<=y)
