#Ternary Operator(เขียน if else elif ให้สั้นลง)
age = int(input("Enter your age :"))
if age>=15:
    print("Teenager")
else:
    print("Children")
#Ternary Operator  
#"เงื่อนไขเป็นจริง" if expression else "เงื่อนไขอื่นๆ"
age  = int(input("Enter your age :"))
print("Teenager") if age>=15 else print("Children")
